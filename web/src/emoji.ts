export interface EmojiEntry {
  emoji: string;
  name: string;
  /** hex codepoint(s) joined with _ for multi-codepoint sequences */
  cp: string;
  /** true if this emoji has an animated version (yellow faces only) */
  animated?: boolean;
}

export interface EmojiCategory {
  id: string;
  label: string;
  icon: string;
  emojis: EmojiEntry[];
}

function e(emoji: string, name: string, animated?: boolean): EmojiEntry {
  const cp = [...emoji].map((c) => c.codePointAt(0)!.toString(16)).join('_');
  return { emoji, name, cp, animated };
}


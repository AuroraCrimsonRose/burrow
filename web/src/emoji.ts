/** All emojis in a flat list */
export const ALL_EMOJIS = EMOJI_CATEGORIES.flatMap((c) => c.emojis);

/** Quick lookup by emoji character */
const EMOJI_MAP = new Map<string, EmojiEntry>();
for (const entry of ALL_EMOJIS) EMOJI_MAP.set(entry.emoji, entry);

export function getEmojiEntry(emoji: string): EmojiEntry | undefined {
  return EMOJI_MAP.get(emoji);
}

/** Get the Noto static SVG URL for an emoji */
export function getNotoUrl(entry: EmojiEntry): string {
  return `https://fonts.gstatic.com/s/e/notoemoji/latest/${entry.cp}/emoji.svg`;
}

/** Get the Noto animated GIF URL for a face emoji */
export function getNotoAnimatedUrl(entry: EmojiEntry): string {
  return `https://fonts.gstatic.com/s/e/notoemoji/latest/${entry.cp}/512.gif`;
}

/** Recently used emojis — stored in localStorage */
const RECENT_KEY = 'emoji_recent';
const MAX_RECENT = 24;

export function getRecentEmojis(): string[] {
  try {
    return JSON.parse(localStorage.getItem(RECENT_KEY) || '[]');
  } catch { return []; }
}

export function addRecentEmoji(emoji: string): void {
  const recent = getRecentEmojis().filter((e) => e !== emoji);
  recent.unshift(emoji);
  if (recent.length > MAX_RECENT) recent.length = MAX_RECENT;
  localStorage.setItem(RECENT_KEY, JSON.stringify(recent));
}

/** Favorite emojis — stored in localStorage */
const FAVORITES_KEY = 'emoji_favorites';

export function getFavoriteEmojis(): string[] {
  try {
    return JSON.parse(localStorage.getItem(FAVORITES_KEY) || '[]');
  } catch { return []; }
}

export function toggleFavoriteEmoji(emoji: string): boolean {
  const favs = getFavoriteEmojis();
  const idx = favs.indexOf(emoji);
  if (idx >= 0) {
    favs.splice(idx, 1);
    localStorage.setItem(FAVORITES_KEY, JSON.stringify(favs));
    return false;
  } else {
    favs.push(emoji);
    localStorage.setItem(FAVORITES_KEY, JSON.stringify(favs));
    return true;
  }
}

export function isFavoriteEmoji(emoji: string): boolean {
  return getFavoriteEmojis().includes(emoji);
}
